package br.com.cmabreu.model;

public class TabuaMares {
	private String ano;
	private String codTabua;
	private Integer codEstacaoMaregrafica;
	private Integer numComponentes;
	private Float nivelMedio;
	
	public String getAno() {
		return ano;
	}
	
	public void setAno(String ano) {
		this.ano = ano;
	}
	
	public String getCodTabua() {
		return codTabua;
	}
	
	public void setCodTabua(String codTabua) {
		this.codTabua = codTabua;
	}
	
	public Integer getCodEstacaoMaregrafica() {
		return codEstacaoMaregrafica;
	}

	public void setCodEstacaoMaregrafica(Integer codEstacaoMaregrafica) {
		this.codEstacaoMaregrafica = codEstacaoMaregrafica;
	}
	
	public Integer getNumComponentes() {
		return numComponentes;
	}
	
	public void setNumComponentes(Integer numComponentes) {
		this.numComponentes = numComponentes;
	}
	
	public Float getNivelMedio() {
		return nivelMedio;
	}
	
	public void setNivelMedio(Float nivelMedio) {
		this.nivelMedio = nivelMedio;
	}
	
	

}
