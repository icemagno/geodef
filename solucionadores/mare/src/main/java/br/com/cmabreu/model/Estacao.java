package br.com.cmabreu.model;

public class Estacao {
	private String nomeEstacaoMaregrafica;
	private String codEstacaoMaregrafica;
	private String numEstacaoMaregrafica;
	private String latitude;
	private String longitude;
	private String fuso;
	private String tabua;
	private String localOrigem;
	private String instituicao;
	private String numAna;
	private String fluviometrica;
	
	
	public String getInstituicao() {
		return instituicao;
	}

	public void setInstituicao(String instituicao) {
		this.instituicao = instituicao;
	}

	public String getLocalOrigem() {
		return localOrigem;
	}

	public void setLocalOrigem(String localOrigem) {
		this.localOrigem = localOrigem;
	}

	public String getNomeEstacaoMaregrafica() {
		return nomeEstacaoMaregrafica;
	}

	public void setNomeEstacaoMaregrafica(String nomeEstacaoMaregrafica) {
		this.nomeEstacaoMaregrafica = nomeEstacaoMaregrafica;
	}

	public String getCodEstacaoMaregrafica() {
		return codEstacaoMaregrafica;
	}

	public void setCodEstacaoMaregrafica(String codEstacaoMaregrafica) {
		this.codEstacaoMaregrafica = codEstacaoMaregrafica;
	}

	public String getNumEstacaoMaregrafica() {
		return numEstacaoMaregrafica;
	}

	public void setNumEstacaoMaregrafica(String numEstacaoMaregrafica) {
		this.numEstacaoMaregrafica = numEstacaoMaregrafica;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getFuso() {
		return fuso;
	}

	public void setFuso(String fuso) {
		this.fuso = fuso;
	}

	public String getTabua() {
		return tabua;
	}

	public void setTabua(String tabua) {
		this.tabua = tabua;
	}

	public String getNumAna() {
		return numAna;
	}

	public void setNumAna(String numAna) {
		this.numAna = numAna;
	}

	public String getFluviometrica() {
		return fluviometrica;
	}

	public void setFluviometrica(String fluviometrica) {
		this.fluviometrica = fluviometrica;
	}

	
}
